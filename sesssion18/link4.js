let number = Number(prompt("Nhập vào 1 số nguyên: "));
switch (number) {
    case 1:
        alert("số một");
        break;
    case 2:
        alert("số hai");
        break;
    case 3:
        alert("số ba");
        break;
    case 4:
        alert("so bốn");
        break;
    case 5:
        alert("so năm");
        break;
    case 6:
        alert("so sáu");
        break;
    case 7:
        alert("so bảy");
        break;
    case 8:
        alert("so tám");
        break;
    case 9:
        alert("so chín");
        break;
    default:
        alert("số không hợp lệ");
}